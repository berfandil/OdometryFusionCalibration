#!/usr/bin/env python3
# proto_radar_lever.py - FALSIFIABLE prototype: recover a translation-only (Doppler radar) sensor's
# LEVER ARM from real nuScenes radar data using ONLY the linear hand-eye TRANSLATION row, which never
# needs the source's own rotation. De-risks the planned "translation-only-source lever estimator"
# core feature BEFORE it is built (Python tools/ prototype only; NO C++/core, NO doc edits).
#
# THE CLAIM UNDER TEST -----------------------------------------------------------------------------
#   A Doppler radar reports a per-window sensor-frame displacement t_B (which PHYSICALLY contains the
#   lever term, v_sensor = v_ego + omega x lever) but NO rotation (R_B = I, heading-blind). Its lever
#   t_X is recoverable from the hand-eye translation identity  A X = X B  with X = (R_X, t_X):
#
#       (R_A - I) t_X = R_X t_B - t_A
#
#   It needs only the BASE rotation R_A + base translation t_A (here from GT ego_pose) + the KNOWN
#   mount rotation R_X (from the trusted prior) + the source translation t_B. It NEVER uses the radar's
#   own (absent) rotation. Accumulate over many TURN windows -> 3x3 normal equations -> solve t_X.
#   On planar (yaw-only) driving (R_A - I) has the z-axis in its null space -> the z-lever is
#   UNOBSERVABLE but x,y ARE observable. Success target: radar x,y lever within a few cm; z at prior.
#
# DATA: the per-scene radar increment + GT CSVs in nuscenes_run/_per_scene/ (generated by
#   nuscenes_to_csv.py via nuscenes_concat.py --keep-temp). The TRUE mounts (R_X, t_X) are the
#   prior_extrinsic lines in nuscenes_run/mini_concat.ini (yaw pitch roll x y z, radians/m).
#   Multiple scenes give the turn excitation that one 20 s scene lacks.
#
# FALSIFICATION PROBES (mandatory; designed so it CAN fail):
#   (a) WRONG R_X (true mount yaw perturbed +20 deg) -> lever recovery must DEGRADE (proves R_X is the
#       load-bearing trusted input: supply it from the prior, do NOT recover it from a heading-blind source).
#   (b) z stays UNOBSERVABLE on planar driving (tiny N eigenvalue) and the solve leaves z near prior.
#   (c) ACCUMULATION: one short scene under-determines x,y (high condition number); many scenes resolve them.
#   (d) SANITY: the wheel reference (lever ~0) recovers ~0.
#
# Pure ASCII; numpy only; deterministic (no RNG needed -- the radar RANSAC seeding lives upstream).
#
# Usage:
#   python tools/proto_radar_lever.py [run_dir] [--win-s S] [--gate G] [--ridge R] [--base gt|consensus]
#   defaults: run_dir=nuscenes_run, win-s=1.0, gate=0.05 rad, ridge=1e-3, base=gt
