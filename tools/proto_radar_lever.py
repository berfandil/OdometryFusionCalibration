# ---------------- linear algebra helpers (w-first quaternions, mirror the existing protos) -------
def quat_to_R(qw, qx, qy, qz):
    n = math.sqrt(qw * qw + qx * qx + qy * qy + qz * qz) or 1.0
    qw, qx, qy, qz = qw / n, qx / n, qy / n, qz / n
    return np.array([[1 - 2 * (qy * qy + qz * qz), 2 * (qx * qy - qz * qw), 2 * (qx * qz + qy * qw)],
                     [2 * (qx * qy + qz * qw), 1 - 2 * (qx * qx + qz * qz), 2 * (qy * qz - qx * qw)],
                     [2 * (qx * qz - qy * qw), 2 * (qy * qz + qx * qw), 1 - 2 * (qx * qx + qy * qy)]])


def so3_log(R):
    c = max(-1.0, min(1.0, (np.trace(R) - 1) / 2.0))
    th = math.acos(c)
    if th < 1e-12:
        return np.zeros(3)
    return np.array([R[2, 1] - R[1, 2], R[0, 2] - R[2, 0], R[1, 0] - R[0, 1]]) * (th / (2 * math.sin(th)))


def Rz(a):
    c, s = math.cos(a), math.sin(a)
    return np.array([[c, -s, 0], [s, c, 0], [0, 0, 1]])


def Ry(a):
    c, s = math.cos(a), math.sin(a)
    return np.array([[c, 0, s], [0, 1, 0], [-s, 0, c]])


def Rx(a):
    c, s = math.cos(a), math.sin(a)
    return np.array([[1, 0, 0], [0, c, -s], [0, s, c]])


def euler_to_R(yaw, pitch, roll):  # Rz(yaw)Ry(pitch)Rx(roll) -- the config_loader convention
    return Rz(yaw) @ Ry(pitch) @ Rx(roll)


# ---------------- CSV loaders ----------------
def load_increments(path):
    # increment CSV: t_ns, x,y,z, qw,qx,qy,qz, 6var. Return list of (t_ns, t(3), R(3x3)).
    out = []
    with open(path) as f:
        for line in f:
            s = line.strip()
            if not s or s.startswith("#"):
                continue
            v = s.split(",")
            t_ns = int(v[0])
            t = np.array([float(v[1]), float(v[2]), float(v[3])])
            R = quat_to_R(float(v[4]), float(v[5]), float(v[6]), float(v[7]))
            out.append((t_ns, t, R))
    return out


def load_gt(path):
    # absolute GT poses: t_ns, x,y,z, qw..qz. Return (ts[np], pos[Nx3], Rs[list of 3x3]).
    ts, pos, Rs = [], [], []
    with open(path) as f:
        for line in f:
            s = line.strip()
            if not s or s.startswith("#"):
                continue
            v = s.split(",")
            ts.append(int(v[0]))
            pos.append([float(v[1]), float(v[2]), float(v[3])])
            Rs.append(quat_to_R(float(v[4]), float(v[5]), float(v[6]), float(v[7])))
    return np.array(ts, dtype=np.int64), np.array(pos), Rs


def parse_priors(ini_path):
    # Return {short: (R_X(3x3), t_X(3), (yaw,pitch,roll))} from prior_extrinsic lines.
    priors = {}
    cur_csv = None
    with open(ini_path) as f:
        for line in f:
            s = line.strip()
            if s.startswith("csv ="):
                cur_csv = s.split("=", 1)[1].strip()
            elif s.startswith("prior_extrinsic ="):
                vals = [float(x) for x in s.split("=", 1)[1].split()]
                yaw, pitch, roll, lx, ly, lz = vals
                # csv name like mini_concat_radar_front.csv -> radar_front
                base = os.path.basename(cur_csv)[:-4]
                idx = base.find("radar_")
                short = base[idx:] if idx >= 0 else base
                priors[short] = (euler_to_R(yaw, pitch, roll),
                                 np.array([lx, ly, lz]), (yaw, pitch, roll))
    return priors


# ---------------- GT pose interpolation within a scene ----------------
def gt_pose_at(ts, pos, Rs, t_query):
    # Linear-interp position; nearest-pose rotation (windows are >=1 s so rotation interp error is
    # negligible vs the >0.05 rad turn gate; nearest keeps it simple and robust). Returns (R, p).
    i = int(np.searchsorted(ts, t_query))
    if i <= 0:
        return Rs[0], pos[0]
    if i >= len(ts):
        return Rs[-1], pos[-1]
    t0, t1 = ts[i - 1], ts[i]
    a = 0.0 if t1 == t0 else (t_query - t0) / float(t1 - t0)
    p = pos[i - 1] * (1 - a) + pos[i] * a
    R = Rs[i - 1] if a < 0.5 else Rs[i]
    return R, p


def base_delta_gt(ts, pos, Rs, t0, t1):
    # Base delta over [t0,t1] in the t0-ego frame:
    #   R_A = R0^T R1 ;  t_A = R0^T (p1 - p0)
    R0, p0 = gt_pose_at(ts, pos, Rs, t0)
    R1, p1 = gt_pose_at(ts, pos, Rs, t1)
    R_A = R0.T @ R1
    t_A = R0.T @ (p1 - p0)
    return R_A, t_A


def base_delta_consensus(inc, t0, t1):
    # Base delta over [t0,t1] by integrating a source's own increments (wheel or imu consensus).
    # inc = list of (t_ns, t(3), R(3x3)) increments. Accumulate increments with t_ns in (t0, t1].
    R = np.eye(3)
    p = np.zeros(3)
    for (t_ns, td, Rd) in inc:
        if t_ns <= t0 or t_ns > t1:
            continue
        p = R @ td + p
        R = R @ Rd
    return R, p


# ---------------- window builder: sum radar increments per [t0,t1] tile, pair with base delta -----
def build_windows(radar_inc, base_fn, win_ns, gate, scene_span_ns):
    # Tile the scene into [t0, t0+win_ns) windows. For each: sum radar increments (R_B ~ I so the
    # sensor-frame displacement is the running R-rotated sum, but since R stays ~I we sum directly),
    # get the base delta, gate on ||log R_A|| > gate. Return list of (R_A, t_A, t_B, logRA_norm).
    wins = []
    t0 = 0
    # index radar increments for fast windowing
    idx = 0
    n = len(radar_inc)
    while t0 < scene_span_ns:
        t1 = t0 + win_ns
        # sum radar sensor-frame displacement over the window. R_B accumulates too (should stay ~I).
        Rb = np.eye(3)
        tb = np.zeros(3)
        any_row = False
        for (t_ns, td, Rd) in radar_inc:
            if t_ns <= t0 or t_ns > t1:
                continue
            tb = Rb @ td + tb
            Rb = Rb @ Rd
            any_row = True
        if any_row:
            R_A, t_A = base_fn(t0, t1)
            nrm = float(np.linalg.norm(so3_log(R_A)))
            if nrm > gate:
                wins.append((R_A, t_A, tb, nrm))
        t0 = t1
    return wins


def solve_lever(wins, R_X, t_prior, ridge):
    # Accumulate N = sum (R_A - I)^T (R_A - I), g = sum (R_A - I)^T (R_X t_B - t_A).
    # Solve t_X = (N + ridge*tr(N)/3 * I) \ (g + ridge*tr(N)/3 * t_prior)  (ridge toward the prior).
    N = np.zeros((3, 3))
    g = np.zeros(3)
    for (R_A, t_A, t_B, _nrm) in wins:
        M = R_A - np.eye(3)
        rhs = R_X @ t_B - t_A
        N += M.T @ M
        g += M.T @ rhs
    ev = np.linalg.eigvalsh(N)  # ascending; per-axis observability
    lam = ridge * (np.trace(N) / 3.0 if np.trace(N) > 0 else 1.0)
    t_X = np.linalg.solve(N + lam * np.eye(3), g + lam * t_prior)
    return t_X, ev, N


def solve_lever_scale(wins, R_X, t_prior, ridge):
    # JOINT lever + Doppler scale (mirrors proto_lever_scale): unknowns u = [t_X; kappa], kappa = 1/scale
    #   (R_A - I) t_X - kappa (R_X t_B) = -t_A
    # The radar Doppler has a per-stream scale error; folding it in absorbs the dominant along-track
    # systematic instead of letting it leak into the x-lever. Returns (t_X, scale, ev4).
    AtA = np.zeros((4, 4))
    Atb = np.zeros(4)
    for (R_A, t_A, t_B, _nrm) in wins:
        J = np.hstack([R_A - np.eye(3), (-(R_X @ t_B)).reshape(3, 1)])
        AtA += J.T @ J
        Atb += J.T @ (-t_A)
    ev = np.linalg.eigvalsh(AtA)
    lam = ridge * (np.trace(AtA) / 4.0 if np.trace(AtA) > 0 else 1.0)
    reg = lam * np.eye(4)
    breg = lam * np.array([t_prior[0], t_prior[1], t_prior[2], 1.0])  # ridge kappa toward 1 (scale 1)
    u = np.linalg.solve(AtA + reg, Atb + breg)
    scale = 1.0 / u[3] if abs(u[3]) > 1e-9 else float("inf")
    return u[:3], scale, ev


def synth_windows_from_gt(scene_dir, scenes, R_X, t_plant, win_ns, gate):
    # CONTROL: build IDEAL windows from GT only, synthesizing the perfect sensor displacement
    #   t_B = R_X^T [ t_A + (R_A - I) t_plant ]
    # so the only thing under test is the linear estimator's math/code (no real-sensor noise). A
    # correct estimator must recover t_plant to ~machine precision from these windows.
    wins = []
    for sc in scenes:
        gpath = os.path.join(scene_dir, "%s_gt.csv" % sc)
        if not os.path.exists(gpath):
            continue
        ts, pos, Rs = load_gt(gpath)
        span = ts[-1] if len(ts) else 0
        t0 = 0
        while t0 < span:
            t1 = t0 + win_ns
            R_A, t_A = base_delta_gt(ts, pos, Rs, t0, t1)
            nrm = float(np.linalg.norm(so3_log(R_A)))
            if nrm > gate:
                t_B = R_X.T @ (t_A + (R_A - np.eye(3)) @ t_plant)
                wins.append((R_A, t_A, t_B, nrm))
            t0 = t1
    return wins


# ---------------- per-radar evaluation over a set of scenes ----------------
def accumulate_wins(run_dir, scene_dir, scenes, radar, base_mode, win_ns, gate):
    # Build (and concatenate) turn windows for one radar across the given scenes. base_mode in
    # {gt, consensus}. Returns list of windows.
    all_wins = []
    for sc in scenes:
        rpath = os.path.join(scene_dir, "%s_%s.csv" % (sc, radar))
        if not os.path.exists(rpath):
            continue
        radar_inc = load_increments(rpath)
        span = radar_inc[-1][0] if radar_inc else 0
        if base_mode == "gt":
            ts, pos, Rs = load_gt(os.path.join(scene_dir, "%s_gt.csv" % sc))
            base_fn = lambda a, b, ts=ts, pos=pos, Rs=Rs: base_delta_gt(ts, pos, Rs, a, b)
        else:
            wheel = load_increments(os.path.join(scene_dir, "%s_wheel.csv" % sc))
            imu = load_increments(os.path.join(scene_dir, "%s_imu.csv" % sc))
            # consensus base = imu rotation (heading-grade) + imu translation; imu carries the heading
            base_fn = lambda a, b, imu=imu: base_delta_consensus(imu, a, b)
        all_wins += build_windows(radar_inc, base_fn, win_ns, gate, span)
    return all_wins


